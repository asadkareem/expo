const mongoose = require('mongoose');
const crypto = require('crypto');
const validator = require('validator');
const bcrypt = require('bcryptjs');
const catchAsync = require('../utils/catchAsync');

const userSchema = new mongoose.Schema({
  name: {
    type: String,
    required: [true, 'Please tell us your name'],
  },
  email: {
    type: String,
    required: [true, 'Please provide the emails'],
    unique: true,
    validate: [validator.isEmail, 'Please prove a valid email'],
  },
  catagory: {
    type: String,
    required: [true, 'Please provide a your catagory'],
  },
  contact: {
    type: String,
    // required: [true, 'Please provide the contact number'],
  },
  photo: String,
  role: {
    type: String,
    enum: ['user', 'admin'],
    default: 'user',
  },
  password: {
    type: String,
    required: [true, 'Please provide a password'],
    minlength: 8,
    select: false,
  },

  //message sent from the  admin
  adminSentMsgCount: {
    type: Number,
    default: 0,
  },
  //message sent from admin to me
  userSentMsgCount: {
    type: Number,
    default: 0,
  },
  messageTime: {
    type: String,
  },
  time: String,
  passwordConfirm: {
    type: String,
    required: [true, 'Please conform your password'],
    validate: {
      validator: function (el) {
        return el == this.password;
      },
      message: 'passwords are not same',
    },
  },
  subScriptionDate: {
    type: Date,
    default: '2023-06-02T00:00:00.000Z',
  },
  fcm_token: {
    type: String
  },
  passwordChangedAt: Date,
  passwordResetToken: String,
  passwordResetExpires: Date,
  active: {
    type: Boolean,
    default: true,
    select: false,
  },
});

userSchema.index({ email: 1 });
userSchema.pre('save', async function (next) {
  // Only run this function if password was actually modified
  if (!this.isModified('password')) return next();

  // Hash the password with cost of 12
  this.password = await bcrypt.hash(this.password, 12);

  // Delete passwordConfirm field
  this.passwordConfirm = undefined;
  next();
});

userSchema.pre(/^find/, function (next) {
  // this points to the current query
  this.find({ active: { $ne: false } });
  next();
});

userSchema.methods.correctPassword = async function (
  candidatePassword,
  userPassword
) {
  return await bcrypt.compare(candidatePassword, userPassword);
};
userSchema.methods.changedPasswordAfter = function (JWTTimestamp) {
  if (this.passwordChangedAt) {
    const changedTimestamp = parseInt(
      this.passwordChangedAt.getTime() / 1000,
      10
    );

    return JWTTimestamp < changedTimestamp;
  }

  // False means NOT changed
  return false;
};
